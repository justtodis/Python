# Para poner comillas dentro de un string hay varias formas

curso = 'Ultimate "Python"' # Comillando el string con comilla simple
print(curso)

curso = "Ultimate \"Python\"" # Utilizando la barra antes del primer comillado y despues de la ultima palabra comillada
print(curso)

curso = "Ultimate \nPython\""  # Con esa n lo pasariamos siguiente linea
print(curso)